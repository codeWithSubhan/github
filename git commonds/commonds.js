// Git is a powerful version control system used for tracking changes in software development projects.
// Here are some important Git commands that every developer should know:

// git init:
// Initializes a new Git repository in the current directory.

// git clone [repository_url]:
// Creates a copy of a remote repository on your local machine.

// git add [file(s)]:
// Stages changes for commit. You can use git add . to stage all changes.

// git commit -m "[commit_message]":
// Commits the staged changes with a descriptive message.

// git status:
// Shows the status of your working directory, including tracked/untracked files and changes.

// git log:
// Displays a history of commits, including commit messages, authors, and timestamps.

// git branch:
// Lists all branches in your repository. The current branch is highlighted with an asterisk (*).

// git checkout [branch_name]:
// Switches to a different branch.

// git checkout -b [new_branch_name]:
// Creates a new branch and switches to it.

// git merge [branch_name]:
// Merges changes from a specified branch into the current branch.

// git pull:
// Fetches changes from a remote repository and merges them into the current branch.

// git push:
// Pushes your local commits to a remote repository.

// git remote -v:
// Lists remote repositories and their URLs.

// git fetch:
// Fetches changes from a remote repository without merging them into your local branch.

// git stash:
// Temporarily saves changes that are not ready for commit.

// git reset [file]:
// Unstages changes in the staging area for a specific file.

// git reset --hard [commit_hash]:
// Resets the repository to a specific commit, discarding all changes after that commit.

// git diff:
// Shows the differences between your working directory and the last commit.

// git tag [tag_name]:
// Creates a lightweight tag for a specific commit.

// git remote add [remote_name] [remote_url]:
// Adds a new remote repository.
